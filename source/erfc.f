      function erfc(x)
c-----------------------------------------------------------------------
c     This routine returns complementary error function erfc(x)
c     with fractional error less than 3d-14.
c     Coded by H.Akai, 26 May 1996, Osaka
c-----------------------------------------------------------------------
      implicit real*8 (a-h,o-z)
      t=2d0/(2d0+abs(x))
      erfc=t*exp((((((((((((((((((((((
     &      1.720080168350883d-2*t-4.371376296748282d-1)*t
     &     +3.745079181764434d0)*t-1.736387283452179d1)*t
     &     +5.143795150558910d1)*t-1.052566450333790d2)*t
     &     +1.548304468785697d2)*t-1.669745098957540d2)*t
     &     +1.333147715485726d2)*t-7.973122163936382d1)*t
     &     +3.677497141832484d1)*t-1.353722950773757d1)*t
     &     +3.722344099228486d0)*t-6.976947513767626d-1)*t
     &     +2.705729177093115d-1)*t+1.210976697829456d-2)*t
     &     -9.016756993157313d-2)*t-1.438575964007708d-1)*t
     &     -8.593274267440335d-2)*t+8.333320401428425d-2)*t
     &     +3.750000018756113d-1)*t+9.999999999889752d-1)*t
     &     -1.265512123484633d0-x*x)
      if(x .lt. 0d0) erfc=2d0-erfc
      end
